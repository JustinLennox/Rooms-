import UIKit

extension UIColor{
    
    //The color from the penguin landing screen photo, MAIN BLUE COLOR
    class func icyBlue() -> UIColor {
        return UIColor(red: 120.0/255.0, green: 204.0/255.0, blue: 207.0/255.0, alpha: 1.0)
    }
    
    class func flatGray() -> UIColor {
        return UIColor(red: 149.0/255.0, green: 165.0/255.0, blue: 166.0/255.0, alpha: 1.0)
    }

    class func suggestionRed() -> UIColor {
        return UIColor(red: 247.0/255.0, green: 98.0/255.0, blue: 94.0/255.0, alpha: 1.0)
    }
    
    class func suggestionDarkRed() -> UIColor {
        return UIColor(red: 192.0/255.0, green: 57.0/255.0, blue: 43.0/255.0, alpha: 1.0)
    }

    class func suggestionBlue() -> UIColor{
        return UIColor(red: 52.0/255.0, green: 152.0/255.0, blue: 219.0/255.0, alpha: 1.0)
    }

    class func suggestionYellow() -> UIColor{
        return UIColor(red: 241.0/255.0, green: 196.0/255.0, blue: 15.0/255.0, alpha: 1.0)
    }
    
    class func suggestionBlack() -> UIColor{
        return UIColor(red: 44.0/255.0, green: 62.0/255.0, blue: 80.0/255.0, alpha: 1.0)
    }

    class func suggestionGreen() -> UIColor{
        return UIColor(red: 39.0/255.0, green: 174.0/255.0, blue: 96.0/255.0, alpha: 1.0)
    }
}
