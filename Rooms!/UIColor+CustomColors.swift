import UIKit

extension UIColor {
    
    //extension
    class func pBlue() ->UIColor
    {
        return UIColor(red: 33/255, green: 150.0/255.0, blue: 243.0/255.0, alpha: 1.0)
        
    }//pBlue
    
}//UIColor